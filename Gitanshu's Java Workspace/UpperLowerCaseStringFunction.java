// I will be using toUpperCase() function to print a string in Upper Case letters and toLowerCase() function to print a string in Lower Case words.


public class UpperLowerCaseStringFunction {
	public static void main(String[] args) {
		
		String aHelloWorld = "Hello World";
		System.out.println("Given String : "+aHelloWorld);
		System.out.println("Given String with toLowerCase() function :"+aHelloWorld.toLowerCase()+" (In Lower Case)");
		System.out.println("Given String with toUpperCase() function :"+aHelloWorld.toUpperCase()+" (In Upper Case)");
	}
}
	