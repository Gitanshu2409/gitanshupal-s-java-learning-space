//Swapping the value of variables 'a' and 'b' by using a temporary variable 'temp'//

public class VariableSwapping {
	public static void main(String[] args) {
		String a;
		String b;
		String temp;
		a = "Pizza";
		b = "Halwa";
		temp = a;
		a = b;
		b = temp;
		System.out.println("Value of a: "+a);
		System.out.println("Value of b: "+b);
		
	}

}

