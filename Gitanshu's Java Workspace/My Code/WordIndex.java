// Index of first letter of a Word in a alloted String
//
public class WordIndex {
	public static void main(String[] args) {
		// The indexOf("Text Here") fuction can be used to find the index of first letter/alphabet of given text in a alloted string.
		String mybasicString = "Hi How are you doing.....";
		System.out.println("This is the String : "+mybasicString);
		System.out.println("The index of first word of phrase 'are' in the given string is : "+mybasicString.indexOf("are"));
	}
}